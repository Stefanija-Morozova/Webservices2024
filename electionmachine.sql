CREATE TABLE CANDIDATES (
   CANDIDATE_ID INTEGER NOT NULL,
   FIRST_NAME VARCHAR(30),
   LAST_NAME VARCHAR(30),
   PARTY VARCHAR(60),
   MUNICIPALITY VARCHAR(50),
   AGE INTEGER,
   DESCRIPTION VARCHAR(4000),
   PRIMARY KEY (CANDIDATE_ID));

